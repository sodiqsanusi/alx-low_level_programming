#include "3-calc.h"

/**
* op_add - Adds two numbers
* @a: First integer
* @b: Second integer
*
* Return: "int" Sum of the two numbers
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - Substract two numbers
* @a: First integer
* @b: Second integer
*
* Return: "int" Difference of the two numbers
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - Multiplies two numbers
* @a: First integer
* @b: Second integer
*
* Return: "int" Product of the two numbers
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - Divides a number by the other
* @a: First integer
* @b: Second integer
*
* Return: "int" Result of @a / @b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - Gets the remainder of division btw two numbers
* @a: First integer
* @b: Second integer
*
* Return: "int" Remainder of division btw @a / @b
*/
int op_mod(int a, int b)
{
	return (a % b);
}

